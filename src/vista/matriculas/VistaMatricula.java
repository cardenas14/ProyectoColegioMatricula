    void buscarMatricula() {
        SimpleDateFormat sdt = new SimpleDateFormat("yyyy-MM-dd");
        String texto_buscar, tipo_matriculas_id, tipo_comprobantes_id, fecDesde, fecHasta;
        texto_buscar = this.txt_bus.getText();
        tipo_matriculas_id = (this.cbx_tpmat.getItemAt(this.cbx_tpmat.getSelectedIndex())).getId();
        tipo_comprobantes_id = (this.cbx_tpcom.getItemAt(this.cbx_tpcom.getSelectedIndex())).getId();
        fecDesde = this.jdc_de.getDate() != null ? sdt.format(this.jdc_de.getDate()) + " 00:00:00" : null;
        fecHasta = this.jdc_hasta.getDate() != null ? sdt.format(this.jdc_hasta.getDate()) + " 23:59:59" : null;
        this.mtm = new MatriculaTableModel(this.mc.index(texto_buscar, tipo_matriculas_id, tipo_comprobantes_id, fecDesde, fecHasta));
        this.tbl_datos.setModel(mtm);
    }
    
    void llenarTipoMatricula() {
        DefaultComboBoxModel<TipoMatricula> comboBoxModel = new DefaultComboBoxModel<>();
        List<TipoMatricula> tipoMatriculas = new TipoMatriculaControlador().index("");
        comboBoxModel.addElement(new TipoMatricula(null, "TODOS", null, null));
        tipoMatriculas.forEach((tp) -> {
            comboBoxModel.addElement(tp);
        });
        this.cbx_tpmat.setModel(comboBoxModel);
    }
    
    void llenarTipoComprobante() {
        DefaultComboBoxModel<TipoComprobante> comboBoxModel = new DefaultComboBoxModel<>();
        List<TipoComprobante> tipoComprobantes = new TipoComprobanteControlador().index("");
        comboBoxModel.addElement(new TipoComprobante(null, "TODOS", null, null, null));
        tipoComprobantes.forEach((tp) -> {
            comboBoxModel.addElement(tp);
        });
        this.cbx_tpcom.setModel(comboBoxModel);
    }
    
    void limpiarFechas() {
        this.jdc_de.setDate(null);
        this.jdc_hasta.setDate(null);
    }

