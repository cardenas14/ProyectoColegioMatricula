    private void jMenuItem2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItem2ActionPerformed
        int fila = this.tbl_datos.getSelectedRow();
        if (fila >= 0) {
            Matricula matricula = this.mtm.getMatriculaAt(fila);
            if (matricula.getFechaAnulacion() == null) {
                new VistaAnularMatricula(this.usuario, matricula).setVisible(true);
            } else {
                JOptionPane.showMessageDialog(rootPane, "La MatrÃ­cula ya se encuentra Anulada.");
            }
        }
    }//GEN-LAST:event_jMenuItem2ActionPerformed

    private void jMenuItem3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItem3ActionPerformed
        int fila = this.tbl_datos.getSelectedRow();
        if (fila >= 0) {
            Matricula matricula = this.mtm.getMatriculaAt(fila);
            JasperReport jr = null;
            String archivo = "C:/Users/Novoa/Documents/NetBeansProjects/YovanaProyectoFinal/src/reportes/FichaMatricula.jasper";
            try {
                jr = (JasperReport) JRLoader.loadObjectFromFile(archivo);
            } catch (JRException ex) {
                Logger.getLogger(VistaMatricula.class.getName()).log(Level.SEVERE, null, ex);
            }
            Map parametros = new HashMap();
            parametros.put("parameter1", matricula.getId());
            try {
                JasperPrint jp = JasperFillManager.fillReport(jr, parametros, Conexion.abrir_conexion());
                JasperViewer jv = new JasperViewer(jp, false);
                jv.setTitle("Ficha de Matricula");
                jv.setVisible(true);
            } catch (JRException ex) {
                Logger.getLogger(VistaMatricula.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_jMenuItem3ActionPerformed

